import mongoose from 'mongoose';

const CaseSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    caseType: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

// Avoid model overwrite in dev
export default mongoose.models.Case || mongoose.model('Case', CaseSchema);
